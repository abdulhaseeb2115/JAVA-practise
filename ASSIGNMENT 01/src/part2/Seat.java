package part2;
public class Seat {
    
	private String seattype;
    private boolean seatbookstat;

//****************************************************************************//
//****************************************************************************//
   
    public Seat(){
    }
    
//****************************************************************************//
//****************************************************************************//
      
    public String SetSeatType(String type){
        
    	if (type.equalsIgnoreCase("SMOKING") || type.equalsIgnoreCase("NONSMOKING") ){
              
    		this.seattype=type;
            return type;
        }
        
    	else{
        return null;
        }

    } 
  
//****************************************************************************//
//****************************************************************************//
   
    public String GetSeatType(){
        
    	return this.seattype;
    }
    
//****************************************************************************//
//****************************************************************************//
     
    
    public boolean Bookseat(boolean bookstat){
        
    	if (bookstat==true){
        
    		this.seatbookstat=bookstat;
        }
        return bookstat;
    }
    
//****************************************************************************//
//****************************************************************************//
     
    public boolean Unbookseat(boolean bookstat){
        
    	if (bookstat==false){
        
    		this.seatbookstat=bookstat;
        }

        return bookstat;

    }
    
//****************************************************************************//
//****************************************************************************//
   
}
